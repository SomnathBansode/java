package day3;

public class _04_Alphabets_A_to_z {
    public static void main(String[] args) {
        for(char i='A'; i<='Z';i++){
            System.out.println("Alphabet is " + i);
        }
        for(char i='a'; i<='z';i++){
            System.out.println("Alphabet is " + i);
        }
    }
}
